const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
   let result='';
   for(let a=0; a<expr.length; a=a+10) {
        let lett='';
        if(expr[a] === '*') {
            result+=' ';
        }
        else {
            for(let b=a; b<a+10; b=b+2) {
                if(+expr[b] === 1 && +expr[b+1]===0) {
                    lett+='.';
                }
                if(+expr[b] === 1 && +expr[b+1]===1) {
                    lett+='-';
                }
            }
           result+=MORSE_TABLE[lett];
        }
    }
        return result;
}

module.exports = {
    decode
}